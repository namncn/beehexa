				$.get('<?php echo admin_url( 'admin-ajax.php' ); ?>?action=post_view_number&nonce='+$nonce+'&p='+$id, function( html ) {
					$(t).html( '<i class="fa fa-eye"></i> ' + html );
				});
			});
		});
	})(jQuery);
	</script>
	<?php
}
